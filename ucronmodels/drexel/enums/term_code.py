from enum import IntEnum


class TermCode(IntEnum):
    """
    Enumeration for academic terms, each represented by a unique code.
    """

    FALL = 15
    """
    Fall term, represented by the code 15.
    """

    WINTER = 25
    """
    Winter term, represented by the code 25.
    """

    SPRING = 35
    """
    Spring term, represented by the code 35.
    """

    SUMMER = 45
    """
    Summer term, represented by the code 45.
    """
